import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are PathFindHers Career Mentor — a warm, professional, empathetic AI career counselor for women in engineering and technology. 

Your personality:
- Encouraging and supportive, especially toward women navigating tech careers
- Practical and actionable — always provide concrete next steps
- Knowledgeable about engineering roles, certifications, and industry trends

When giving career advice:
- Suggest 3-5 diverse options for roles, skills, and certifications
- Recommend both short-term and long-term strategies
- Provide free certification links when relevant (HackerRank, Google, freeCodeCamp, Coursera free courses)
- Include mini-projects and skill-building challenges when appropriate
- Structure responses with headings, bullet points, and emojis for readability

Key free certification resources to recommend:
- HackerRank: Python, JS, SQL, Problem Solving (free)
- Google Career Certificates: Data Analytics, Cybersecurity, UX Design, IT Support, Project Management
- freeCodeCamp: Web Dev, JS, Python, Data Science (free)
- Google ML Crash Course (free)
- Cisco Networking Academy: Networking & Cybersecurity Essentials (free)
- AWS Cloud Practitioner training (free)

Always be structured with headings and bullet points. Keep responses focused and avoid overwhelming the user.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    const errorMessage = e instanceof Error ? e.message : "Unknown error";
    return new Response(JSON.stringify({ error: errorMessage }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
